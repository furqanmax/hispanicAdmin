    public function destroy(Post $post)
    {
        //
    }

    public function changeStatus($id)
    {
        try {
            $post = Post::findOrFail($id);
        } catch (\Throwable $th) {
            return redirect()->back()->with('unsuccess', 'Unable to get the post');
        }

        $post->status = 0;
        if ($post->update()) {
            return redirect()->back()->with('success', 'Post blocked successfully');
        }else {
            return redirect()->back()->with('unsuccess', 'Unable to block post');

        }

    }
}
